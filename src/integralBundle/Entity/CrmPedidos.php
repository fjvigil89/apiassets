<?php

namespace integralBundle\Entity;

use Doctrine\ORM\Mapping as ORM;

/**
 * CrmPedidos
 *
 * @ORM\Table(name="CRM_Pedidos")
 * @ORM\Entity
 */
class CrmPedidos
{
    /**
     * @var integer
     *
     * @ORM\Column(name="Contador_Pe", type="integer", nullable=false)
     * @ORM\Id
     * @ORM\GeneratedValue(strategy="IDENTITY")
     */
    private $contadorPe;

    /**
     * @var integer
     *
     * @ORM\Column(name="Id_Pedido", type="integer", nullable=false)
     */
    private $idPedido;

    /**
     * @var string
     *
     * @ORM\Column(name="Id_Almacen", type="string", length=5, nullable=false)
     */
    private $idAlmacen;

    /**
     * @var integer
     *
     * @ORM\Column(name="Ano_Pedido", type="smallint", nullable=false)
     */
    private $anoPedido;

    /**
     * @var \DateTime
     *
     * @ORM\Column(name="Fecha_Pedido", type="datetime", nullable=false)
     */
    private $fechaPedido;

    /**
     * @var string
     *
     * @ORM\Column(name="Id_Cliente", type="string", length=15, nullable=false)
     */
    private $idCliente;

    /**
     * @var integer
     *
     * @ORM\Column(name="ImporteMB", type="integer", nullable=false)
     */
    private $importemb;

    /**
     * @var integer
     *
     * @ORM\Column(name="ImporteMC", type="integer", nullable=false)
     */
    private $importemc;

    /**
     * @var integer
     *
     * @ORM\Column(name="Contador1", type="integer", nullable=false)
     */
    private $contador1;



    /**
     * Get contadorPe
     *
     * @return integer 
     */
    public function getContadorPe()
    {
        return $this->contadorPe;
    }

    /**
     * Set idPedido
     *
     * @param integer $idPedido
     * @return CrmPedidos
     */
    public function setIdPedido($idPedido)
    {
        $this->idPedido = $idPedido;

        return $this;
    }

    /**
     * Get idPedido
     *
     * @return integer 
     */
    public function getIdPedido()
    {
        return $this->idPedido;
    }

    /**
     * Set idAlmacen
     *
     * @param string $idAlmacen
     * @return CrmPedidos
     */
    public function setIdAlmacen($idAlmacen)
    {
        $this->idAlmacen = $idAlmacen;

        return $this;
    }

    /**
     * Get idAlmacen
     *
     * @return string 
     */
    public function getIdAlmacen()
    {
        return $this->idAlmacen;
    }

    /**
     * Set anoPedido
     *
     * @param integer $anoPedido
     * @return CrmPedidos
     */
    public function setAnoPedido($anoPedido)
    {
        $this->anoPedido = $anoPedido;

        return $this;
    }

    /**
     * Get anoPedido
     *
     * @return integer 
     */
    public function getAnoPedido()
    {
        return $this->anoPedido;
    }

    /**
     * Set fechaPedido
     *
     * @param \DateTime $fechaPedido
     * @return CrmPedidos
     */
    public function setFechaPedido($fechaPedido)
    {
        $this->fechaPedido = $fechaPedido;

        return $this;
    }

    /**
     * Get fechaPedido
     *
     * @return \DateTime 
     */
    public function getFechaPedido()
    {
        return $this->fechaPedido;
    }

    /**
     * Set idCliente
     *
     * @param string $idCliente
     * @return CrmPedidos
     */
    public function setIdCliente($idCliente)
    {
        $this->idCliente = $idCliente;

        return $this;
    }

    /**
     * Get idCliente
     *
     * @return string 
     */
    public function getIdCliente()
    {
        return $this->idCliente;
    }

    /**
     * Set importemb
     *
     * @param integer $importemb
     * @return CrmPedidos
     */
    public function setImportemb($importemb)
    {
        $this->importemb = $importemb;

        return $this;
    }

    /**
     * Get importemb
     *
     * @return integer 
     */
    public function getImportemb()
    {
        return $this->importemb;
    }

    /**
     * Set importemc
     *
     * @param integer $importemc
     * @return CrmPedidos
     */
    public function setImportemc($importemc)
    {
        $this->importemc = $importemc;

        return $this;
    }

    /**
     * Get importemc
     *
     * @return integer 
     */
    public function getImportemc()
    {
        return $this->importemc;
    }

    /**
     * Set contador1
     *
     * @param integer $contador1
     * @return CrmPedidos
     */
    public function setContador1($contador1)
    {
        $this->contador1 = $contador1;

        return $this;
    }

    /**
     * Get contador1
     *
     * @return integer 
     */
    public function getContador1()
    {
        return $this->contador1;
    }
}
