     * @var string
     *
     * @ORM\Column(name="Mayo", type="decimal", precision=14, scale=4, nullable=true)
     */
    private $mayo;

    /**
     * @var string
     *
     * @ORM\Column(name="Junio", type="decimal", precision=14, scale=4, nullable=true)
     */
    private $junio;

