     * Set agosto
     *
     * @param string $agosto
     * @return Estadistica
     */
    public function setAgosto($agosto)
    {
        $this->agosto = $agosto;

        return $this;
    }

    /**
     * Get agosto
     *
     * @return string 
     */
    public function getAgosto()
    {
        return $this->agosto;
