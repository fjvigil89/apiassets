<?php

namespace integralBundle\Entity;

use Doctrine\ORM\Mapping as ORM;

/**
 * Estadistica
 *
 * @ORM\Table(name="Estadistica")
 * @ORM\Entity
 */
class Estadistica
{
    /**
     * @var string
     *
     * @ORM\Column(name="Clasificacion", type="string", length=30, nullable=false)
     * @ORM\Id
     * @ORM\GeneratedValue(strategy="IDENTITY")
     */
