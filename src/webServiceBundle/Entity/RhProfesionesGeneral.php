<?php

namespace webServiceBundle\Entity;

use Doctrine\ORM\Mapping as ORM;

/**
 * RhProfesionesGeneral
 *
 * @ORM\Table(name="RH_Profesiones_General")
 * @ORM\Entity
 */
class RhProfesionesGeneral
{
    /**
     * @var string
     *
     * @ORM\Column(name="Id_Especialidad", type="string", length=3, nullable=false)
     * @ORM\Id
     * @ORM\GeneratedValue(strategy="IDENTITY")
     */
    private $idEspecialidad;

    /**
     * @var string
     *
     * @ORM\Column(name="Desc_Especialidad", type="string", length=25, nullable=false)
     */
    private $descEspecialidad;


}
